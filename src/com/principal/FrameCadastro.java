    public void cadastro(){
        DataHora hora = new DataHora();  
        Login log = new Login();    
        log.setUser(matriculaTxt.getText());
        log.setNome(conferenteTxt.getText().toUpperCase());
        log.setPassword("novousuario");
        log.setData_cadastro(hora.Data());
        log.setRole(roleTxt.getText());
        LoginJpaDAO.getInstance().persist(log);
        
        
        tabela();
        matriculaTxt.setText("");
        conferenteTxt.setText("");
        matriculaTxt.requestFocus(true);
    }
    
    
    public void verifiqueUsuario(){
      
        List<Login> log = LoginJpaDAO.getInstance().findAll();
        String matricula = "";
        
        for(int b = 0; b < log.size(); b ++){
          
         String tempMat = "";
         tempMat = log.get(b).getUser().replace("", "");
         tempMat.replace("", "");
         matricula = tempMat;
         
         if(matriculaTxt.getText().toUpperCase().equals(matricula)){
             JOptionPane.showMessageDialog(null, "Matricula jÃ¡ cadastrada!");    
             break;
         }
           conferenteTxt.requestFocus(true);
        }
        
        
    }
    
    
    public void tabela(){
        try{
           ArrayList dados = new ArrayList();
           String[] Colunas = new String[]{"Codigo","Matricula", "Usuario", "Data de Cadastro"};
           List<Login> rt = LoginJpaDAO.getInstance().findAll();
           for(int b = 0; b < rt.size(); b ++){
               dados.add(new Object[]{rt.get(b).getId(), rt.get(b).getUser(), rt.get(b).getNome(), rt.get(b).getData_cadastro()});
           }
           Tabela tabela = new Tabela(dados, Colunas);
           tblRoteiros.setModel(tabela);
           tblRoteiros.setRowSorter(new TableRowSorter(tabela));
           tblRoteiros.getColumnModel().getColumn(0).setPreferredWidth(80);
           tblRoteiros.getColumnModel().getColumn(0).setResizable(false);
           tblRoteiros.getColumnModel().getColumn(1).setPreferredWidth(120);
           tblRoteiros.getColumnModel().getColumn(1).setResizable(false);
           tblRoteiros.getColumnModel().getColumn(2).setPreferredWidth(340);
           tblRoteiros.getColumnModel().getColumn(2).setResizable(false);
           tblRoteiros.getColumnModel().getColumn(3).setPreferredWidth(180);
           tblRoteiros.getColumnModel().getColumn(3).setResizable(false);
           tblRoteiros.getTableHeader().setReorderingAllowed(false);
           tblRoteiros.setAutoResizeMode(tblRoteiros.AUTO_RESIZE_OFF);
           tblRoteiros.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
                       
        }catch(Exception ex){
          ex.printStackTrace();
        }
      }

    
     public void excluirDados() {
        /**
         * getValueAt devolve o valor de uma linha/coluna no principio de por exemplo : linha 1 coluna 1
         * getSelectedRow() devolve um valor int com numero indice da linha selecionada
         * getSelectedColumn() devolve um valor int com numero indice da coluna selecionada
         */
        int idUser =  (int) tblRoteiros.getValueAt(tblRoteiros.getSelectedRow(), tblRoteiros.getSelectedColumn());
        
        LoginJpaDAO.getInstance().removeById(idUser);
        System.out.println("Linha removida...");
        
        tabela();
        System.out.println("Tabela atualizada...");
        
    }
     


